# Types of inheritance

    # single inheritance
    # multiple inheritance
    # hybrid inheritance

# single inheritance
# what is single inheritance?
    # single inheritance is when a class inherits from only one class

# multiple inheritance
# what is multiple inheritance?
    # multiple inheritance is when a class inherits from more than one class

    # multiple inheritance is used to access the variables and methods of the parent class

# hybrid inheritance
# what is hybrid inheritance?
    # hybrid inheritance is when a class inherits from both the parent and child class